namespace App\Http\Middleware\Admin;

use Closure;
use App;

class Language
{

    /**
     * Handle an incoming request.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \Closure  $next
     * @return mixed
     */
    public function handle($request, Closure $next)
    {
        App::setLocale('zh-CN');
        return $next($request);
    }

}
